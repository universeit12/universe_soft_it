


class FacultyMember {
  final String _id;

  final String _thumbnailUrl;

  final String _name;

  final String _email;

  FacultyMember(this._id, this._thumbnailUrl, this._name, this._email);





  String get id => _id;

  String get name => _name;

  String get email => _email;

  String get thumbnailUrl => _thumbnailUrl;




}
